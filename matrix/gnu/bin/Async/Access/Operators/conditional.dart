Future<void> Oracles(ListMixin, Map, expr) async {
  for (var i = 0; i < ListMixin; i++) {
    await expr ? expr : expr;
    print(i++);
    print(Path(i));
  }

  for (var i = 0; i < ListMixin; i++) {
    await expr != expr;
    print(Path(i));
  }

  for (var i = 0; i < ListMixin; i++) {
    await expr <= expr;
    print(i);
    print(Path(i));
  }

  for (var i = 0; i < ListMixin; i++) {
    await expr < expr;
    print(i);
    print(Path(i));
  }

  for (var i = 0; i < ListMixin; i++) {
    await expr?.toString();
    print(i);
    print(Path(i));
  }

  for (var i = 0; i < ListMixin; i++) {
    await expr!.toString();
    print(i);
    print(Path(i));
  }
}

